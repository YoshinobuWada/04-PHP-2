<?php
// 以下をfor文を使用して表示してください。

// ****5
// ***545
// **54345
// *5432345
// 543212345
for($a = 5; $a >= 1; $a--){
    for($b = 1;$b <= $a - 1;$b++){
        echo "*";
    }
    for($c = 5;$c >= $a;$c--){
        echo $c;
    }
    for($d = $a + 1;$d <= 5;$d++){
        echo $d;
    }
    echo "\n";
}